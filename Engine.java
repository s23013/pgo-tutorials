package PGO;

    class Engine {

        private int hp;
        private int year;

        public Engine(int hp, int year) {
            this.hp = hp;
            this.year = year;
        }

        public int getHp() {
            return hp;
        }

        public void setHp(int hp) {
            this.hp = hp;
        }

        public int getYear() {
            return year;
        }

        public void setYear(int year) {
            this.year = year;
        }
    }
